# EXERCÍCIO
# Faça um programa que, dada a temperatura T em graus Fahrenheit, transforme a temperatura em graus Celsius.
# T = 100ºF
# Fórmula: C = (5/9) * (F - 32)

# Código para converter temperatura de Fahrenheit para Celsius
Fahrenheit = 100
print(f"A temperatura em graus Fahrenheit é: {Fahrenheit}°F")

Celsius = (5/9) * (Fahrenheit - 32)
print(f"A temperatura em graus Celsius é: {Celsius:.2f}°C")
